create procedure [dbo].[usp_InventoryControl_CycleCount_MarkListedObjects]
	@User varchar(10)
,	@CycleCountNumber varchar(50)
,	@NewStatus int
,	@SerialList varchar(max)
,	@TranDT datetime = null out
,	@Result integer = null out
as
set nocount on
set ansi_warnings off
set	@Result = 999999

--- <Error Handling>
declare
	@CallProcName sysname,
	@TableName sysname,
	@ProcName sysname,
	@ProcReturn integer,
	@ProcResult integer,
	@Error integer,
	@RowCount integer

set	@ProcName = user_name(objectproperty(@@procid, 'OwnerId')) + '.' + object_name(@@procid)  -- e.g. dbo.usp_Test
--- </Error Handling>

--- <Tran Required=Yes AutoCreate=Yes TranDTParm=Yes>
declare
	@TranCount smallint

set	@TranCount = @@TranCount
if	@TranCount = 0 begin
	begin tran @ProcName
end
else begin
	save tran @ProcName
end
set	@TranDT = coalesce(@TranDT, GetDate())
--- </Tran>

---	<ArgumentValidation>

---	</ArgumentValidation>

--- <Body>
/*	Parse serial list. */
create table #serialList
(	serial int
,	RowID int not null IDENTITY(1, 1) primary key
)

insert
	#serialList
select
	serial = convert(int, fsstr.value)
from
	dbo.fn_SplitStringToRows(@SerialList, ',') fsstr
where
	fsstr.Value like '%[0-9]%'
	and fsstr.Value not like '%[^0-9]%'

/*	Call mark object procedure. */
--- <Call>	
set	@CallProcName = 'dbo.usp_InventoryControl_CycleCount_MarkObject'
execute
	@ProcReturn = dbo.usp_InventoryControl_CycleCount_MarkObject
		@User = @User
	,	@CycleCountNumber = @CycleCountNumber
	,	@NewStatus = @NewStatus
	,	@Serial = null
	,	@TranDT = @TranDT out
	,	@Result = @ProcResult out

set	@Error = @@Error
if	@Error != 0 begin
	set	@Result = 900501
	RAISERROR ('Error encountered in %s.  Error: %d while calling %s', 16, 1, @ProcName, @Error, @CallProcName)
	rollback tran @ProcName
	return	@Result
end
if	@ProcReturn != 0 begin
	set	@Result = 900502
	RAISERROR ('Error encountered in %s.  ProcReturn: %d while calling %s', 16, 1, @ProcName, @ProcReturn, @CallProcName)
	rollback tran @ProcName
	return	@Result
end
if	@ProcResult != 0 begin
	set	@Result = 900502
	RAISERROR ('Error encountered in %s.  ProcResult: %d while calling %s', 16, 1, @ProcName, @ProcResult, @CallProcName)
	rollback tran @ProcName
	return	@Result
end
--- </Call>
--- </Body>

---	<CloseTran AutoCommit=Yes>
if	@TranCount = 0 begin
	commit tran @ProcName
end
---	</CloseTran AutoCommit=Yes>

---	<Return>
set	@Result = 0
return
	@Result
--- </Return>

