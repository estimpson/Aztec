create procedure [dbo].[usp_MES_NewProduction]
	@Operator varchar (10)
,	@JobID int
,	@QtyToLabel numeric(20,6)
,	@QtyPerBox int
,	@PackageCode varchar(25) = null
,	@LotNumber varchar(20) = null
,	@FirstNewSerial int out
,	@Boxes int out
,	@TranDT datetime out
,	@Result integer out
as
set nocount on
set ansi_warnings off
set	@Result = 999999

--- <Error Handling>
declare
	@CallProcName sysname,
	@TableName sysname,
	@ProcName sysname,
	@ProcReturn integer,
	@ProcResult integer,
	@Error integer,
	@RowCount integer

set	@ProcName = user_name(objectproperty(@@procid, 'OwnerId')) + '.' + object_name(@@procid)  -- e.g. dbo.usp_Test
--- </Error Handling>

--- <Tran Required=Yes AutoCreate=Yes TranDTParm=Yes>
declare
	@TranCount smallint

set	@TranCount = @@TranCount
if	@TranCount = 0 begin
	begin tran @ProcName
end
else begin
	save tran @ProcName
end
set	@TranDT = coalesce(@TranDT, GetDate())
--- </Tran>

---	<ArgumentValidation>

---	</ArgumentValidation>

--- <Body>
/*	Create pre-objects for the desired quantity. */
--- <Call>
set	@CallProcName = 'dbo.usp_MES_NewPreObject'
execute
	@ProcReturn = dbo.usp_MES_NewPreObject 
		@Operator = @Operator
	,	@WODID = @JobID
	,	@QtyToLabel = @QtyToLabel
	,	@QtyBox = @QtyPerBox
	,	@PackageCode = @PackageCode
	,	@LotNumber = @LotNumber
	,	@FirstNewSerial = @FirstNewSerial out
	,	@Boxes = @Boxes out
	,	@TranDT = @TranDT out
	,	@Result = @Result out

set	@Error = @@Error
if	@Error != 0 begin
	set	@Result = 900501
	RAISERROR ('Error encountered in %s.  Error: %d while calling %s', 16, 1, @ProcName, @Error, @CallProcName)
	rollback tran @ProcName
	return	@Result
end
if	@ProcReturn != 0 begin
	set	@Result = 900502
	RAISERROR ('Error encountered in %s.  ProcReturn: %d while calling %s', 16, 1, @ProcName, @ProcReturn, @CallProcName)
	rollback tran @ProcName
	return	@Result
end
if	@ProcResult != 0 begin
	set	@Result = 900502
	RAISERROR ('Error encountered in %s.  ProcResult: %d while calling %s', 16, 1, @ProcName, @ProcResult, @CallProcName)
	rollback tran @ProcName
	return	@Result
end
--- </Call>

/*	Loop through pre-objects and complete them. */
declare
	@JCSerial int = @FirstNewSerial

while
	@JCSerial <= @FirstNewSerial + @Boxes begin

	--- <Call>
	set	@CallProcName = 'dbo.usp_MES_JCPreObject'
	execute
		@ProcReturn = dbo.usp_MES_JCPreObject
			@Operator = @Operator
		,	@PreObjectSerial = @JCSerial
		,	@TranDT = @TranDT out
		,	@Result = @ProcResult out

	set	@Error = @@Error
	if	@Error != 0 begin
		set	@Result = 900501
		RAISERROR ('Error encountered in %s.  Error: %d while calling %s', 16, 1, @ProcName, @Error, @CallProcName)
		rollback tran @ProcName
		return	@Result
	end
	if	@ProcReturn != 0 begin
		set	@Result = 900502
		RAISERROR ('Error encountered in %s.  ProcReturn: %d while calling %s', 16, 1, @ProcName, @ProcReturn, @CallProcName)
		rollback tran @ProcName
		return	@Result
	end
	if	@ProcResult != 0 begin
		set	@Result = 900502
		RAISERROR ('Error encountered in %s.  ProcResult: %d while calling %s', 16, 1, @ProcName, @ProcResult, @CallProcName)
		rollback tran @ProcName
		return	@Result
	end
	--- </Call>

	set	@JCSerial = @JCSerial + 1
end
--- </Body>

---	<Return>
set	@Result = 0
return
	@Result
--- </Return>

