create procedure [dbo].[usp_MES_CloseJob]
	@JobID int
,	@TranDT datetime out
,	@Result integer out
as
set nocount on
set ansi_warnings off
set	@Result = 999999

--- <Error Handling>
declare
	@CallProcName sysname,
	@TableName sysname,
	@ProcName sysname,
	@ProcReturn integer,
	@ProcResult integer,
	@Error integer,
	@RowCount integer

set	@ProcName = user_name(objectproperty(@@procid, 'OwnerId')) + '.' + object_name(@@procid)  -- e.g. dbo.usp_Test
--- </Error Handling>

--- <Tran Required=Yes AutoCreate=Yes TranDTParm=Yes>
declare
	@TranCount smallint

set	@TranCount = @@TranCount
if	@TranCount = 0 begin
	begin tran @ProcName
end
else begin
	save tran @ProcName
end
set	@TranDT = coalesce(@TranDT, GetDate())
--- </Tran>

---	<ArgumentValidation>

---	</ArgumentValidation>

--- <Body>
/*	Close job.*/
declare
	@WorkOrderNumber varchar(50)
,	@WorkOrderDetailLine float

select
	@WorkOrderNumber = wod.WorkOrderNumber
,	@WorkOrderDetailLine = wod.Line
from
	dbo.WorkOrderDetails wod
where
	RowID = @JobID

-- <Update rows="1+">
set	@TableName = 'dbo.WorkOrderDetails'
	
update
	wod
set
	Status = dbo.udf_StatusValue('dbo.WorkOrderDetails', 'Deleted')
from
	dbo.WorkOrderDetails wod
where
	wod.WorkOrderNumber = @WorkOrderNumber
	and wod.Line = @WorkOrderDetailLine

select
	@Error = @@Error,
	@RowCount = @@Rowcount

if	@Error != 0 begin
	set	@Result = 999999
	RAISERROR ('Error updating table %s in procedure %s.  Error: %d', 16, 1, @TableName, @ProcName, @Error)
	rollback tran @ProcName
	return
end
if	@RowCount <= 0 begin
	set	@Result = 999999
	RAISERROR ('Error updating into %s in procedure %s.  Rows Updated: %d.  Expected rows: 1 or more.', 16, 1, @TableName, @ProcName, @RowCount)
	rollback tran @ProcName
	return
end
-- </Update>
	
-- <Update rows="1+">
set	@TableName = 'dbo.WorkOrderHeaders'

update
	woh
set
	Status = dbo.udf_StatusValue('dbo.WorkOrderHeaders', 'Deleted')
from
	dbo.WorkOrderHeaders woh
where
	woh.WorkOrderNumber = @WorkOrderNumber

select
	@Error = @@Error,
	@RowCount = @@Rowcount

if	@Error != 0 begin
	set	@Result = 999999
	RAISERROR ('Error updating table %s in procedure %s.  Error: %d', 16, 1, @TableName, @ProcName, @Error)
	rollback tran @ProcName
	return
end
if	@RowCount <= 0 begin
	set	@Result = 999999
	RAISERROR ('Error updating into %s in procedure %s.  Rows Updated: %d.  Expected rows: 1 or more.', 16, 1, @TableName, @ProcName, @RowCount)
	rollback tran @ProcName
	return
end
-- </Update>
--- </Body>

---	<Return>
set	@Result = 0
return
	@Result
--- </Return>

